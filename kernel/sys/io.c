#define FONT_WIDTH 8
#define FONT_HEIGHT 16

#define SCR_WIDTH 800
#define SCR_HEIGHT 600

uint16_t *coms = (uint16_t*)0x400;
uint16_t *lpts = (uint16_t*)0x408;

void putpixel(uint16_t x, uint16_t y, uint32_t color) {
    uint8_t a = (color>>24) & 0xFF;
    uint32_t where = (y*SCR_WIDTH+x) * 3;
    uint32_t r = color & 0xFF, g = (color>>8) & 0xFF, b = (color>>16) & 0xFF;
    r = vbe->framebuffer[where] * (255-a) + r*a;
    g = vbe->framebuffer[where+1] * (255-a) + g*a;
    b = vbe->framebuffer[where+2] * (255-a) + b*a;
    vbe->framebuffer[where] = r/255;
    vbe->framebuffer[where+1] = g/255;
    vbe->framebuffer[where+2] = b/255;
}

int initSerial(uint16_t port) {
    outb(port + 1, 0x00);
    outb(port + 3, 0x80);
    outb(port + 3, 0x03);
    outb(port + 2, 0xC7);
    outb(port + 4, 0x0B);
    outb(port + 4, 0x1E);
    outb(port + 0, 0xAE);

    if(inb(port + 0) != 0xAE) {
        return 1;
    }
    outb(port + 4, 0x0F); 
    return 0;
}

void writeSerial(uint16_t port, uint8_t c) {
    outb(port, c);
}

uint8_t readSerial(uint16_t port) {
    return inb(port);
}

int scr_x = 0;
int scr_y = 0;
TermColor color = WHITE;

void swipeTerm() {
    memcpy(vbe->framebuffer, &vbe->framebuffer[(SCR_WIDTH*3)*FONT_HEIGHT], SCR_WIDTH*SCR_HEIGHT*3);
    memset(&vbe->framebuffer[(scr_y*SCR_WIDTH+scr_x) * 3], 0, SCR_WIDTH*FONT_HEIGHT*3);
}

void newLine() {
    scr_y += FONT_HEIGHT;
    scr_x = 0;
    if (scr_y > SCR_HEIGHT - FONT_HEIGHT) {
        scr_y -= FONT_HEIGHT;
        swipeTerm();
    }
}

void setColor(TermColor c) {
    color = c;
}

int putchar(int c) {
    if (c == '\n') {
        newLine();
        return (uint8_t)c;
    }
    

    uint8_t *b = &_3dfx8x16[c*FONT_HEIGHT];

    for(int i = 0; i < FONT_HEIGHT; i++) {
        uint8_t letter = b[i];
        for(int j = 0; j < FONT_WIDTH; j++) {
            uint8_t part = (letter>>(FONT_WIDTH-1-j))&1;
            if (part) {
                putpixel(scr_x+j, scr_y+i, color);
            }
        }
    } 


    scr_x += FONT_WIDTH;
    if (scr_x == SCR_WIDTH) {
        newLine();
    }
    return (uint8_t)c;
}

void backspace() {
    if(!scr_y) return;
    if(scr_x == 0) {
        scr_x = SCR_WIDTH;
        scr_y--;
    }
    scr_x--;
//    SCR_AT(scr_x, scr_y, 0) = ' ';
//    SCR_AT(scr_x, scr_y, 1) = PINK;
}
